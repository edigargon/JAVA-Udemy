package expressoesComparativas;

/*
Operadores:
< Menor
> Maior
<= Menor Igual
>= Maior Igual
== Igual
!= Diferente
 */

public class Main {
    public static void main(String[] args) {

        int x = 5;

        System.out.println(x > 0);
        System.out.println(x == 3);
        System.out.println(10 <= 30);
        System.out.println(x != 2);

    }
}